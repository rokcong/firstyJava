package Ch_01;

public class _02_DataTypes {
    public static void main(String[] args) {  // public static void main(String[] args) -> main || psvm 약어로 명령어 가능
        System.out.println("Hello World");  // 문자열    // System.out.println(); -> sout 약어로 명령어 가능
        System.out.println("안녕하세요?");   // 문자열
        System.out.println(12);            // 정수(숫자)형
        System.out.println(-34);           // 음수(숫자)형
        System.out.println(3.14);          // 실수(숫자)형
        System.out.println(true);          // boolean 형
        System.out.println(false);         // boolean 형

        System.out.println(123 + 345);
    }
}
